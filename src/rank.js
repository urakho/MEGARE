// Compute rank and star count from trophy total.
function getRankInfo(trophies) {
    let rank = TANK_RANKS[0];
    for (let i = TANK_RANKS.length - 1; i >= 0; i--) {
        if (trophies >= TANK_RANKS[i].base) { rank = TANK_RANKS[i]; break; }
    }
    let stars = 0;
    if (rank.perStar > 0) {
        stars = Math.min(3, Math.floor((trophies - rank.base) / rank.perStar));
    }
    return {
        name: rank.name,
        icon: rank.icon,
        color: rank.color,
        stars,
        trophies,
        perStar: rank.perStar,
        base: rank.base,
    };
}
window.getRankInfo = getRankInfo;

