// Determine outcome type based on battle metrics
function determineOutcomeType(isWin) {
    if (isWin) {
        const battleDuration = gameStartTime ? (Date.now() - gameStartTime) / 1000 : 0; // seconds
        const playerDamage = Math.max(0, playerMaxHpAtStart - tank.hp);
        const damagePercent = playerMaxHpAtStart > 0 ? (playerDamage / playerMaxHpAtStart) : 0;
        
        // prioritize by SPEED first: fast > crushing > hard > normal
        
        // Fast win: < 90s for bossfight, < 30s others
        const fastWinThreshold = currentMode === 'bossfight' ? 90 : 30;
        if (battleDuration < fastWinThreshold) {
            return 'fast';
        }
        // Crushing win: took minimal damage (< 30%) - domination
        else if (damagePercent < 0.3) {
            return 'crushing';
        }
        // Hard win: took ZERO damage (perfect victory)
        else if (playerDamage === 0) {
            return 'hard';
        }
        // Normal win
        return 'normal';
    } else {
        const battleDuration = gameStartTime ? (Date.now() - gameStartTime) / 1000 : 0; // seconds
        
        // Fast loss: died in first 30s for bossfight, first 15s otherwise
        const fastLossThreshold = currentMode === 'bossfight' ? 30 : 15;
        if (battleDuration < fastLossThreshold) {
            return 'quick';
        }
        // Bossfight: overwhelm if boss entered phase 2
        if (currentMode === 'bossfight') {
            const boss = enemies.find(e => e.isBoss);
            if (boss && boss.isPhase2) return 'overwhelm';
        }
        // Check if nearest enemy is very low on HP (almost win condition)
        const nearestEnemyHp = enemies.length > 0
            ? Math.max(...enemies.filter(e => e && e.hp && e.hp > 0).map(e => e.hp || 0))
            : 0;
        if (nearestEnemyHp > 0 && nearestEnemyHp <= 2) {
            return 'almost';
        }
        // Overwhelm: multiple enemies nearby (3+)
        const nearbyCount = enemies.filter(e => {
            if (!e || !e.x || !e.y) return false;
            const dist = Math.hypot(tank.x - e.x, tank.y - e.y);
            return dist < 300; // within 300 pixels
        }).length;
        if (nearbyCount >= 3) {
            return 'overwhelm';
        }
        // Normal loss
        return 'normal';
    }
}
